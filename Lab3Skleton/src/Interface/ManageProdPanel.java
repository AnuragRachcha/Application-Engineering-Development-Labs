    private void btnSearchNameActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchNameActionPerformed
        // TODO add your handling code here:
         
        List<Product> p = prodDir.searchName(searchNameText.getText());
         if (searchNameText.getText() != null) {
      
        if (p == null){
            JOptionPane.showMessageDialog(null, "Invalid Product name");
        }
        else {
            DefaultTableModel dtm = (DefaultTableModel)tblDirectory.getModel();
            dtm.setRowCount(0);
            for (Product pro : p) {
                Object[] row = new Object[3];
                row[0]=pro;
                row[1]=pro.getAvailNum();
                row[2]=pro.getPrice();
                dtm.addRow(row);
                
            }
        }
    }   else {
              JOptionPane.showMessageDialog(null, "Please enter a name!");
          }
    }//GEN-LAST:event_btnSearchNameActionPerformed

    private void btnViewProductActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewProductActionPerformed
        // TODO add your handling code here:
         int selectedRow = tblDirectory.getSelectedRow();
         if (selectedRow>=0) {
             Product product = (Product)tblDirectory.getValueAt(selectedRow, 0);
             ViewPanel vp = new ViewPanel(rightPanel,product);
             rightPanel.add("ViewPanel", vp);
             CardLayout layout = (CardLayout)rightPanel.getLayout();
             layout.next(rightPanel);
             
         } else {
              JOptionPane.showMessageDialog(null, "Select a row");
         }
    }//GEN-LAST:event_btnViewProductActionPerformed

